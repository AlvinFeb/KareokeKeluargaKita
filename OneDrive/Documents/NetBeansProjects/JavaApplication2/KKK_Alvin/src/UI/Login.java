    private void loginbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbuttonActionPerformed
            String username = usernamefield.getText().trim();
    char[] passwordChars = passwordfield.getPassword();
    String password = new String(passwordChars);
    
    // Clear the password array immediately
    Arrays.fill(passwordChars, '\0');
    
    // Validate inputs
    if (username.isEmpty() || username.equals("Enter your username here")) {
        JOptionPane.showMessageDialog(this, "Please enter your username", "Error", JOptionPane.ERROR_MESSAGE);
        usernamefield.requestFocus();
        return;
    }
    
    if (password.isEmpty() || password.equals("Password")) {
        JOptionPane.showMessageDialog(this, "Please enter your password", "Error", JOptionPane.ERROR_MESSAGE);
        passwordfield.requestFocus();
        return;
    }
    
    UserLoginDAO userDAO = new UserLoginDAO();
    try {
        String userRole = userDAO.authenticateUser(username, password);
        
        if (userRole != null) {
            JOptionPane.showMessageDialog(this, "Login Successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
            UserSession.setUsername(username);
            UserSession.setRole(userRole);
            
            // Open appropriate panel based on role
            if ("admin".equalsIgnoreCase(userRole)) {
                AdminPanel adminPanel = new AdminPanel();
                adminPanel.setVisible(true);
            } else {
                Homepage main = new Homepage();
                main.setVisible(true);
            }
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Username or Password", 
                "Login Failed", JOptionPane.ERROR_MESSAGE);
            resetPasswordField();
            usernamefield.requestFocus();
        }
    } finally {
        userDAO.closeConnection();
    }


    }//GEN-LAST:event_loginbuttonActionPerformed

